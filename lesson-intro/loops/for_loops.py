# for loops allow to perform an action for each iterable element in a collection

ingredients = ["milk", "sugar", "vanilla extract", "dough", "chocolate"]

for ingredient in ingredients:
    print(ingredient)

# for loops can be combined with range to specify a number of actions with
# an arbiturary list

for i in range(6):
    print("Pass",str(i+1),"Loops pratice")


